function cleanWord(w) {
  return w.replace(/[^a-záàâãéêíóôõúüçñA-ZÁÀÂÃÉÊÍÓÔÕÚÜÇÑ]/g, '');
}

function normalize(s) {
  return s
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z]/g, '');
}

function countOccurrences(word) {
  const norm = normalize(word);
  return STORY_RAW
    .split(/\s+/)
    .filter(t => normalize(cleanWord(t)) === norm)
    .length;
}


function countByLength(len) {
  return STORY_RAW
    .split(/\s+/)
    .filter(t => cleanWord(t).length === len)
    .length;
}

