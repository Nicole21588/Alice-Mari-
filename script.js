/**

 * @param {number|null} hlLength  
 * @param {string|null} hlSearch 
 * @param {string[]|null} hlChars 
 */
function renderText(hlLength, hlSearch, hlChars) {
  const paragraphs = STORY_RAW.split('\n').filter(p => p.trim());
  const normSearch  = hlSearch ? normalize(hlSearch) : null;
  const normChars   = hlChars  ? hlChars.map(normalize) : null;
  let html = '';

  paragraphs.forEach(para => {
    html += '<p>';
    para.split(/(\s+)/).forEach(token => {
      if (/^\s+$/.test(token)) { html += token; return; }

      const clean   = cleanWord(token);
      const len     = clean.length;
      if (len === 0) { html += token; return; }

      const normClean = normalize(clean);

      
      let cls = '';
      if (normSearch && normClean === normSearch) {
        cls = 'hl-search';
      } else if (normChars && normChars.includes(normClean)) {
        cls = 'hl-search';
      } else if (hlLength !== null && len === hlLength) {
        if      (len <= 2)  cls = 'hl-1';
        else if (len <= 4)  cls = 'hl-3';
        else if (len <= 6)  cls = 'hl-5';
        else if (len <= 8)  cls = 'hl-7';
        else if (len <= 10) cls = 'hl-9';
        else                cls = 'hl-big';
      }

      if (cls) {
        html += `<span class="word word-span ${cls}"
          data-word="${clean.toLowerCase()}"
          data-len="${len}"
          tabindex="0"
          role="mark"
          aria-label="${clean}, ${len} letras">${token}</span>`;
      } else {
        html += `<span class="word"
          data-word="${clean.toLowerCase()}"
          data-len="${len}">${token}</span>`;
      }
    });
    html += '</p>';
  });

  document.getElementById('story-text').innerHTML = html;
  bindWordEvents();
}

function renderTextBig() {
  const paragraphs = STORY_RAW.split('\n').filter(p => p.trim());
  let html = '';
  paragraphs.forEach(para => {
    html += '<p>';
    para.split(/(\s+)/).forEach(token => {
      if (/^\s+$/.test(token)) { html += token; return; }
      const clean = cleanWord(token);
      if (!clean) { html += token; return; }
      if (clean.length >= 11) {
        html += `<span class="word word-span hl-big"
          data-word="${clean.toLowerCase()}"
          data-len="${clean.length}"
          tabindex="0" role="mark">${token}</span>`;
      } else {
        html += `<span class="word"
          data-word="${clean.toLowerCase()}"
          data-len="${clean.length}">${token}</span>`;
      }
    });
    html += '</p>';
  });
  document.getElementById('story-text').innerHTML = html;
  bindWordEvents();
}


function bindWordEvents() {
  document.querySelectorAll('.word[data-len]').forEach(span => {
    span.addEventListener('mouseenter', showTooltip);
    span.addEventListener('mouseleave', hideTooltip);
    span.addEventListener('focus',      showTooltip);
    span.addEventListener('blur',       hideTooltip);
  });
}


function showTooltip(e) {
  const word  = e.target.dataset.word;
  const len   = e.target.dataset.len;
  if (!word || !len) return;

  const count   = countOccurrences(word);
  const tooltip = document.getElementById('wordTooltip');
  tooltip.textContent = `"${word}" · ${len} letras · aparece ${count}x`;
  tooltip.style.display = 'block';
  positionTooltip(e);
}

function hideTooltip() {
  document.getElementById('wordTooltip').style.display = 'none';
}

function positionTooltip(e) {
  const t = document.getElementById('wordTooltip');
  t.style.left = (e.clientX + 14) + 'px';
  t.style.top  = (e.clientY - 40) + 'px';
}

document.addEventListener('mousemove', e => {
  const t = document.getElementById('wordTooltip');
  if (t.style.display === 'block') positionTooltip(e);
});

function highlightByLength() {
  const val = parseInt(document.getElementById('letterInput').value);
  if (!val || val < 1 || val > 30) {
    setStatus('🐇 Digite um número válido de letras!');
    return;
  }
  clearCharSelection();
  currentLength = val;
  currentSearch = null;
  renderText(val, null, null);

  const count = countByLength(val);
  document.getElementById('resultHighlightText').innerHTML =
    `Encontrei <strong style="color:var(--dourado);font-size:1.3em;">${count}</strong>
     palavra${count !== 1 ? 's' : ''} com
     <strong style="color:var(--dourado)">${val}</strong>
     letra${val !== 1 ? 's' : ''}!`;

  document.getElementById('resultHighlight').classList.add('show');
  document.getElementById('resultCount').classList.remove('show');
  setStatus(`✨ Palavras com ${val} letras destacadas! Total: ${count}`);
  scrollToStory();
  spawnSparkles();
}

function countWord() {
  const val = document.getElementById('wordInput').value.trim();
  if (!val) {
    setStatus('🐇 Digite uma palavra primeiro!');
    return;
  }
  clearCharSelection();
  currentSearch = val;
  currentLength = null;
  renderText(null, val, null);

  const count = countOccurrences(val);
  document.getElementById('countNum').textContent = count;
  document.getElementById('resultCountText').innerHTML =
    count === 0
      ? `A palavra "<strong>${val}</strong>" não aparece no texto.`
      : `vez${count !== 1 ? 'es' : ''} a palavra "<strong>${val}</strong>" aparece!`;

  document.getElementById('resultCount').classList.add('show');
  document.getElementById('resultHighlight').classList.remove('show');

  if (count > 0) {
    setTimeout(() => {
      const first = document.querySelector('.hl-search');
      if (first) first.scrollIntoView({ behavior: 'smooth', block: 'center' });
    }, 100);
    setStatus(`🔍 "${val}" aparece ${count} vez${count !== 1 ? 'es' : ''} no texto!`);
    spawnSparkles();
  } else {
    setStatus(`😢 "${val}" não foi encontrada no texto.`);
  }
}

function quickHighlight(n) {
 
  if (activeChip) activeChip.classList.remove('active-chip');
  document.querySelectorAll('.letter-chip').forEach(c => {
    const label = n === 11 ? '11+' : n + ' letras';
    if (c.textContent.trim().startsWith(label.split(' ')[0])) {
      const isMatch = n === 11
        ? c.textContent.includes('11+')
        : c.textContent.startsWith(n + ' ');
      if (isMatch) { c.classList.add('active-chip'); activeChip = c; }
    }
  });

  document.getElementById('letterInput').value = n;
  clearCharSelection();
  currentSearch = null;

  if (n === 11) {
    currentLength = null;
    renderTextBig();
    const count = STORY_RAW.split(/\s+/).filter(t => cleanWord(t).length >= 11).length;
    document.getElementById('resultHighlightText').innerHTML =
      `Encontrei <strong style="color:var(--dourado);font-size:1.3em;">${count}</strong>
       palavras com <strong style="color:var(--dourado)">11+</strong> letras!`;
    setStatus(`✨ Palavras gigantes (11+ letras) destacadas! Total: ${count}`);
  } else {
    currentLength = n;
    renderText(n, null, null);
    const count = countByLength(n);
    document.getElementById('resultHighlightText').innerHTML =
      `Encontrei <strong style="color:var(--dourado);font-size:1.3em;">${count}</strong>
       palavra${count !== 1 ? 's' : ''} com
       <strong style="color:var(--dourado)">${n}</strong>
       letra${n !== 1 ? 's' : ''}!`;
    setStatus(`✨ Palavras com ${n} letras destacadas! Total: ${count}`);
  }

  document.getElementById('resultHighlight').classList.add('show');
  document.getElementById('resultCount').classList.remove('show');
  scrollToStory();
  spawnSparkles();
}

function filterChar(id) {
  if (activeCharBtn) activeCharBtn.classList.remove('active');
  const btn = document.getElementById('btn-' + id);
  if (btn) { btn.classList.add('active'); activeCharBtn = btn; }

  const keywords = CHAR_KEYWORDS[id] || [];
  currentLength = null;
  currentSearch = null;
  renderText(null, null, keywords);

  const count = keywords.reduce((acc, kw) => acc + countOccurrences(kw), 0);
  setStatus(`🔍 Personagem destacado no texto! Ocorrências: ${count}`);
  document.getElementById('resultHighlight').classList.remove('show');
  document.getElementById('resultCount').classList.remove('show');

  scrollToStory();
  spawnSparkles();
  setTimeout(() => {
    const first = document.querySelector('.hl-search');
    if (first) first.scrollIntoView({ behavior: 'smooth', block: 'center' });
  }, 200);
}

function clearCharSelection() {
  if (activeCharBtn) { activeCharBtn.classList.remove('active'); activeCharBtn = null; }
}

function clearAll() {
  currentLength = null;
  currentSearch = null;
  clearCharSelection();
  if (activeChip) { activeChip.classList.remove('active-chip'); activeChip = null; }
  document.getElementById('letterInput').value = '';
  document.getElementById('wordInput').value   = '';
  document.getElementById('resultHighlight').classList.remove('show');
  document.getElementById('resultCount').classList.remove('show');
  renderText(null, null, null);
  setStatus('🐇 Todos os destaques foram removidos!');
}


